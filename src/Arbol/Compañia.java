package Arbol;

/**
 *
 * @author alexz
 */
public class Compañia {
    
    private String name;
    private String catchPhrase;
    private String bs; 

    public Compañia(String name, String catchPhrase, String bs) {
        this.name = name;
        this.catchPhrase = catchPhrase;
        this.bs = bs;
    }

    public String getName() {
        return name;
    }

    public String getCatchPhrase() {
        return catchPhrase;
    }

    public String getBs() {
        return bs;
    }
    
    
}
